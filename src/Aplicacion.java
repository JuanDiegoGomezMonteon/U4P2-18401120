    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!estaSeleccionada()){
            mensaje("ERROR!\nDebes SELECCIONAR una fila de la tabla");
            return;
        }
        
        int fila = jTable1.getSelectedRow();
        idGlobal = tablin.getValueAt(fila, 0).toString();
        int resp = JOptionPane.showConfirmDialog(this, "Estas seguro que deseas BORRAR ID:"+idGlobal+"?");
        if(resp == JOptionPane.OK_OPTION){
            if(eliminar(idGlobal) == true){
                mensaje("Se ELIMINO correctaente");
                cargarDatos();
            }else{
                mensaje("NO se pudo ELIMINAR el ID:"+idGlobal);
            }
                
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

